age = 47
if age <= 5:
    print("You're a baby!")
elif age <= 12:
    print("You're a child!")
elif age <= 19:
    print("You're a teenager!")
elif age <= 29:
    print("You're a young adult!")
elif age <= 40:
    print("You're in your prime!")
elif age <= 55:
    print("You're middle aged!")
elif age <= 70:
    print("You're old!")
else:
    print("You're elderly")